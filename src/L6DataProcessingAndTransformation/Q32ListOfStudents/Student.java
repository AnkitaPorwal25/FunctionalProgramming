package L6DataProcessingAndTransformation.Q32ListOfStudents;
class Student {
    private String name;
    private int grade;

    // Constructor
    public Student(String name, int grade) {
        this.name = name;
        this.grade = grade;
    }

    // Getters
    public String getName() { return name; }
    public int getGrade() { return grade; }
}
